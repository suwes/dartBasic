import 'package:operator_komparasi/operator_komparasi.dart' as operator_komparasi;

void main(){

  // opersator komparasi
  // akan menghasilkan nilai boolean

  var a = 2;
  var b = 5;

  print('apakah $a == $b = ${a == b}'); // sama dengan ==
  print('apakah $a != $b = ${a != b}'); // tidak sama dengan !=
  print('apakah $a > $b = ${a > b}'); // lebih besar >
  print('apakah $a < $b = ${a < b}'); // lebih kecil <
  print('apakah $a >= $b = ${a >= b}'); // lebih besar atau sama dengan
  print('apakah $a <= $b = ${a <= b}'); // lebih kecil atau sama dengan



}
